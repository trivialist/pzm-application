     public void initjComboDate() {
        jComboBoxEndDay.removeAllItems();
        jComboBoxEndMonth.removeAllItems();
        jComboBoxEndYear.removeAllItems();
        jComboBoxStartDay.removeAllItems();
        jComboBoxStartMonth.removeAllItems();
        jComboBoxStartYear.removeAllItems();
         
         
        int[] year = new int[1101];
        int j = 0;
        
        String[] month = {"Januar", "Februar", "März", "April", "Mai", "Juni",
                          "Juli", "August", "September", "Oktober", "November", 
                          "Dezember"};
        int[] day = new int[31];
        
 
        // ComboBox Year füllen
        jComboBoxStartYear.addItem("<Bitte wählen>");
        jComboBoxEndYear.addItem("<Bitte wählen>");
        for(int i=2000; i<=2050; i++) {
            year[j] = i;
             jComboBoxStartYear.addItem(year[j]);
             jComboBoxEndYear.addItem(year[j]);
            j++;
        }
  
        
        // ComboBox Month füllen
        jComboBoxStartMonth.addItem("<Bitte wählen>");
        jComboBoxEndMonth.addItem("<Bitte wählen>");
        for(int i=0;i<month.length;i++) {
            jComboBoxStartMonth.addItem(month[i]);
            jComboBoxEndMonth.addItem(month[i]);
        }
        // ComboBox Day füllen
        for(int i=0; i<=30; i++) {
            day[i] = i+1;
        }
        jComboBoxStartDay.addItem("<Bitte wählen>");
        jComboBoxEndDay.addItem("<Bitte wählen>");
        for(int i=0;i<day.length;i++) {
            jComboBoxStartDay.addItem(day[i]);
            jComboBoxEndDay.addItem(day[i]);
        }
        
    }
     public boolean isDateSet() {
        
        int year = new Integer(String.valueOf(jComboBoxStartYear.getSelectedItem())).intValue();
        int day = new Integer(String.valueOf(jComboBoxStartDay.getSelectedItem())).intValue();;
        int month = -1;
        String monthString = String.valueOf(jComboBoxStartMonth.getSelectedItem());
        if(monthString.equals("Januar"))    { month = 0; }
        if(monthString.equals("Februar"))   { month = 1; }
        if(monthString.equals("März"))      { month = 2; }
        if(monthString.equals("April"))     { month = 3; }
        if(monthString.equals("Mai"))       { month = 4; }
        if(monthString.equals("Juni"))      { month = 5; }
        if(monthString.equals("Juli"))      { month = 6; }
        if(monthString.equals("August"))    { month = 7; }
        if(monthString.equals("September")) { month = 8; }
        if(monthString.equals("Oktober"))   { month = 9; }
        if(monthString.equals("November"))  { month = 10; }
        if(monthString.equals("Dezember"))  { month = 11; }

        if(!String.valueOf(year).equals("<Bitte wählen>")) {
            if(!String.valueOf(month).equals("<Bitte wählen>")) {
                if(!String.valueOf(day).equals("<Bitte wählen>")) {

                    startDate = ""+year+"-"+month+"-"+day;

                    return true;
                } else {
                    jLabelError.setText("Bitte Tag wählen");
                    return false;
                }
            } else {
                jLabelError.setText("Bitte Monat wählen");
                return false;
            }
        } else {
            jLabelError.setText("Bitte Jahr wählen");
            return false;
        }
    }

