    public void getPrGroupID() {
        DB_projektzeit_Connect dbCon = new DB_projektzeit_Connect();
        dbCon.openDB();
        con = dbCon.getCon();
        
        try {
            Statement stmt = con.createStatement();
            String sql = "SELECT * FROM PrGroup WHERE PrGroup='" + prGroupName + "'";
            ResultSet rst = stmt.executeQuery(sql);

            while(rst.next()) {
                prGroupID = rst.getInt("PrGroupID");
            }
            rst.close();
            stmt.close();
        }
        catch(Exception e) {
            System.out.println(e.toString()); 
            System.exit(1);
        }
        dbCon.closeDB(con);
    }

